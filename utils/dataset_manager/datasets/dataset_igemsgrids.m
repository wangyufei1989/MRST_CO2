function [info, present] = dataset_igemsgrids()

   [info, present] = datasetInfoStruct(...
       'name', 'IGEMS grids', ...
       'website', 'https://www.nr.no/IGEMS/', ...
       'fileurl', 'http://files.nr.no/igems/eclipsegrids.zip', ...
       'hasGrid', true, ...
       'hasRock', true, ...
       'cells', 3600000, ...
       'examples', {}, ...
       'filesize', 470, ...
       'description', [...
           'Grids generated in the IGEMS project to study the impact of ' ...
           'caprock shape on CO2 migration using geologically realistic ' ...
           'models.  NB: This is a large dataset (470 Mb) containing 15 ' ...
           'high-resolution grid models on ECLIPSE format.'], ... 
       'modelType', 'Corner-point');
end
